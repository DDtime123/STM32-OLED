#include "led.h"

/*******************************************************************************
*函数的原型：void Led_Init(void)
*函数的功能：LED初始化
*函数的参数：None
*函数返回值：None
*函数的说明：
	PA0 高电平亮
*函数编写者：
*函数编写日期：2020/12/13
*函数的版本号：V1.0
********************************************************************************/
void Led_Init(void)
{
	RCC->APB2ENR |= (1 << 2);
	
	GPIOA->CRL &= ~(0xf << 0); 
	GPIOA->CRL |= (0X3 << 0); 
	
	GPIOA->ODR &= ~(1 << 0); 
}

